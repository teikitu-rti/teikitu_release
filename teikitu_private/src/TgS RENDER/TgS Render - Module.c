/* ---- tgRN_Query_Init ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgBOOL tgRN_Query_Init( TgVOID )
{
    return (ETgMODULE_STATE__INITIALIZED <= s_enRender_State && s_enRender_State <= ETgMODULE_STATE__STOPPED);
}


/* ---- tgRN_Query_Boot ---------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgBOOL tgRN_Query_Boot( TgVOID )
{
    return (ETgMODULE_STATE__BOOTED == s_enRender_State);
}

