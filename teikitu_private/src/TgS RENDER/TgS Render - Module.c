/* ---- tgRN_Module_Init --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgRESULT tgRN_Module_Init( TgVOID )
{
    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__FREED == s_enRender_State );
    s_enRender_State = ETgMODULE_STATE__INITIALIZING;

    s_enRender_State = ETgMODULE_STATE__INITIALIZED;
    return (KTgS_OK);
}


/* ---- tgRN_Module_Boot --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgRESULT tgRN_Module_Boot( TgVOID )
{
    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__INITIALIZED == s_enRender_State );
    s_enRender_State = ETgMODULE_STATE__BOOTING;

    /*
    LOG_STD_MSG_USZ_FMTU64( KTgCN_CHANEL_INITIALIZE_MEMORY, u8"Static Memory Size", tgRN_Query_Fixed_Memory() )
                 */

    s_enRender_State = ETgMODULE_STATE__BOOTED;
    return (KTgS_OK);
}


/* ---- tgRN_Module_Stop --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgRN_Module_Stop( TgVOID )
{
    if ((ETgMODULE_STATE__FREED == s_enRender_State) || (ETgMODULE_STATE__INITIALIZED == s_enRender_State))
    {
        return;
    };

    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__BOOTED == s_enRender_State );
    s_enRender_State = ETgMODULE_STATE__STOPPING;

    s_enRender_State = ETgMODULE_STATE__STOPPED;
}


/* ---- tgRN_Module_Free --------------------------------------------------------------------------------------------------------------------------------------------------------- */
/* ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- */
TgVOID tgRN_Module_Free( TgVOID )
{
    if (ETgMODULE_STATE__FREED == s_enRender_State)
    {
        return;
    };

    /* Verify the state of the system */
    TgERROR( ETgMODULE_STATE__STOPPED == s_enRender_State || ETgMODULE_STATE__INITIALIZED == s_enRender_State );
    s_enRender_State = ETgMODULE_STATE__FREEING;

    s_enRender_State = ETgMODULE_STATE__FREED;
}


